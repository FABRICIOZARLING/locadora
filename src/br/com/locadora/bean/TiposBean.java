package br.com.locadora.bean;

public class TiposBean {
	private int idTipo;
	private String tipo;
	public int getIdTipo() {
		return idTipo;
	}
	public void setIdTipo(int idTipo) {
		this.idTipo = idTipo;
	}
	public String getNomeTipo() {
		return tipo;
	}
	public void setNomeTipo(String nomeTipo) {
		this.tipo = nomeTipo;
	}

}
